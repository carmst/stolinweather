def load_bet_payloads() -> dict[str, dict[str, Any]]:
    payloads: dict[str, dict[str, Any]] = {}
    snapshot_dir = OUTPUT_DIR / "bets" / "snapshots"
    resolved_dir = OUTPUT_DIR / "bets" / "resolved"

    for path in sorted(snapshot_dir.glob("*.json")):
        payload = load_json(path)
        target_date = payload.get("target_date") or path.stem
        payloads[target_date] = payload

    for path in sorted(resolved_dir.glob("*.json")):
        payload = load_json(path)
        target_date = payload.get("target_date") or path.stem
        payloads[target_date] = payload

    latest_path = OUTPUT_DIR / "bets" / "latest_daily_bets.json"
    if latest_path.exists():
        payload = load_json(latest_path)
        target_date = payload.get("target_date")
        if target_date:
            payloads[target_date] = payload

    return payloads


def as_contract_yes_outcome(bet: dict[str, Any]) -> bool | None:
    bet_won = bet.get("bet_won")
    side = bet.get("recommended_side")
    if bet_won is None or side not in {"yes", "no"}:
        return None
    return bool(bet_won) if side == "yes" else not bool(bet_won)


def parse_event_date(event_ticker: str, title: str | None, close_time: str | None) -> str | None:
    match = re.search(r"-(\d{2})([A-Z]{3})(\d{2})$", event_ticker)
    if match:
        year = 2000 + int(match.group(1))
        month = {
            "JAN": 1,
            "FEB": 2,
            "MAR": 3,
            "APR": 4,
            "MAY": 5,
            "JUN": 6,
            "JUL": 7,
            "AUG": 8,
            "SEP": 9,
            "OCT": 10,
            "NOV": 11,
            "DEC": 12,
        }[match.group(2)]
        day = int(match.group(3))
        return dt.date(year, month, day).isoformat()

    if title:
        title_match = re.search(r"on ([A-Z][a-z]{2} \d{1,2}, \d{4})", title)
        if title_match:
            return dt.datetime.strptime(title_match.group(1), "%b %d, %Y").date().isoformat()

    if close_time:
        return close_time[:10]

    return None


def load_json(path: Path) -> Any:
    return json.loads(path.read_text(encoding="utf-8"))


def deterministic_uuid(*parts: Any) -> uuid.UUID:
    key = "::".join("" if part is None else str(part) for part in parts)
    return uuid.uuid5(UUID_NAMESPACE, key)


def hash_json(value: Any) -> str:
    encoded = json.dumps(value, sort_keys=True, separators=(",", ":"), default=str)
    return hashlib.sha256(encoded.encode("utf-8")).hexdigest()


def sql_value(value: Any) -> str:
    if value is None:
        return "null"
    if isinstance(value, bool):
        return "true" if value else "false"
    if isinstance(value, (int, float)) and not isinstance(value, bool):
        return repr(value)
    if isinstance(value, (dt.date, dt.datetime)):
        return "'" + value.isoformat().replace("'", "''") + "'"
    text = str(value).replace("'", "''")
    return f"'{text}'"


def sql_json(value: Any) -> str:
    return f"{sql_value(json.dumps(value, sort_keys=True))}::jsonb"


def sql_array(values: Iterable[Any]) -> str:
    items = list(values)
    if not items:
        return "array[]::text[]"
    rendered = ", ".join(sql_value(item) for item in items)
    return f"array[{rendered}]"


