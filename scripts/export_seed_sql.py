#!/usr/bin/env python3
"""Export current local JSON artifacts into idempotent Postgres seed SQL."""

from __future__ import annotations

import argparse
import datetime as dt
import hashlib
import json
import re
import uuid
from pathlib import Path
from typing import Any, Iterable


ROOT = Path(__file__).resolve().parent.parent
OUTPUT_DIR = ROOT / "output"
CONFIG_DIR = ROOT / "config"
DB_DIR = ROOT / "db"
DEFAULT_OUTPUT = DB_DIR / "seed_current_state.sql"
UUID_NAMESPACE = uuid.UUID("d54d4f84-a839-45b8-9001-e8daf127fdf2")


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Generate SQL seed data from the project's current JSON outputs."
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=DEFAULT_OUTPUT,
        help=f"Output SQL file (default: {DEFAULT_OUTPUT})",
    )
    args = parser.parse_args()

    sql = build_seed_sql()
    output_path = args.output if args.output.is_absolute() else (ROOT / args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(sql, encoding="utf-8")
    print(f"Wrote seed SQL: {output_path}")
    return 0


def build_seed_sql() -> str:
    tracked_markets = load_json(CONFIG_DIR / "tracked_markets.json")
    settlement_sources = load_json(CONFIG_DIR / "kalshi_settlement_sources.json")
    latest_markets = load_json(OUTPUT_DIR / "kalshi" / "latest_markets.json")
    noaa_weather = load_json(OUTPUT_DIR / "weather" / "latest_forecasts_noaa.json")
    open_meteo_weather = load_json(OUTPUT_DIR / "weather" / "latest_forecasts.json")
    scored_markets = load_json(OUTPUT_DIR / "models" / "latest_scored_markets.json")
    noaa_history = load_json(OUTPUT_DIR / "history" / "latest_noaa_history.json")
    bankroll_history = load_json(OUTPUT_DIR / "bets" / "bankroll" / "bankroll_history.json")

    bet_payloads = load_bet_payloads()

    tracked_by_market = {row["market_id"]: row for row in tracked_markets}
    tracked_by_series = {}
    for row in tracked_markets:
        for series in row.get("kalshi_series", []):
            tracked_by_series[series] = row

    lines: list[str] = []
    emit = lines.append
    emit("-- Generated by scripts/export_seed_sql.py")
    emit(f"-- Source workspace: {ROOT}")
    emit(f"-- Generated at: {dt.datetime.now(dt.timezone.utc).isoformat()}")
    emit("begin;")
    emit("set search_path to app, public;")
    emit("")

    emit_reference_seed(emit, tracked_markets, settlement_sources)
    emit_kalshi_seed(emit, latest_markets, tracked_by_series)
    emit_weather_seed(emit, noaa_weather, open_meteo_weather)
    emit_observation_seed(emit, noaa_history)
    emit_scored_seed(emit, scored_markets)
    emit_daily_bets_seed(emit, bet_payloads)
    emit_bankroll_seed(emit, bankroll_history)

    emit("commit;")
    emit("")
    return "\n".join(lines)


